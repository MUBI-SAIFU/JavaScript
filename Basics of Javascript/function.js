// check whether parameter start with letter 'a'
function isStartWithA(name){
    name=name.toLowerCase() //to take both lowercase and uppercase of A
    return name[0]=="a"?true:false
}
console.log(isStartWithA("jayan"));
console.log(isStartWithA("Aravind"));
